
package Model;

public class HoaDon {
    public long id;
    public String ten;
    public int soluong;
    public int size;
    public long gia;
    public long tt;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getSoluong() {
        return soluong;
    }

    public void setSoluong(int soluong) {
        this.soluong = soluong;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public long getGia() {
        return gia;
    }

    public void setGia(long gia) {
        this.gia = gia;
    }

    public long getTt() {
        return tt;
    }

    public void setTt(long tt) {
        this.tt = tt;
    }
    
    
}
